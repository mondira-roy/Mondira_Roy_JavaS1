package com.company.StorePackage;

public class Restaurant {
    private String SodaPop;
    private String menuCard;
    private String waitStaff;

    public String getSodaPop() {
        return SodaPop;
    }

    public void setSodaPop(String sodaPop) {
        SodaPop = sodaPop;
    }

    public String getMenuCard() {
        return menuCard;
    }

    public void setMenuCard(String menuCard) {
        this.menuCard = menuCard;
    }

    public String getWaitStaff() {
        return waitStaff;
    }

    public void setWaitStaff(String waitStaff) {
        this.waitStaff = waitStaff;
    }
}
